export function linksSugeridosPorTipo(tipo, livroId = null) {
  return LINKS_SUGERIDOS.filter((item) => {
    if (item.tipo !== tipo) return false;
    if (!livroId) return true;
    return !item.livroId || item.livroId === livroId;
  });
}

export function linkSugeridoPorId(id) {
  return LINKS_SUGERIDOS.find((item) => item.id === id) || null;
}

export function urlJaSalva(dados, url) {
  const alvo = (url || "").trim();
  return dados.links.some((l) => l.url.trim() === alvo);
}
